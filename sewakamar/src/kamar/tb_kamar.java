    private void tampildata(String sql){
        DefaultTableModel datalist= new DefaultTableModel();
        datalist.addColumn("No");
        datalist.addColumn("Kode Kamar");
        datalist.addColumn("Nama Kamar");
        datalist.addColumn("Type Kamar");
        datalist.addColumn("Harga Kamar");
        
        try {
            int i = 1;
            st=con.createStatement();
            rsKmr=st.executeQuery("SELECT * FROM tb_kamar");
            while (rsKmr.next()){
                datalist.addRow(new Object[]{
                    (""+i++),
                    rsKmr.getString(1), rsKmr.getString(2), rsKmr.getString(3), 
                    rsKmr.getString(4)
                });
                gridkamar.setModel(datalist);
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, 
                    "Gagal Tampil \n"+e.getMessage());
        }
    }
