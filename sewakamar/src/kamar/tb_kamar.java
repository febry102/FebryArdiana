    private void txtkodekamarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtkodekamarActionPerformed
        // TODO add your handling code here:
        kode=txtkodekamar.getText();
        int tekanenter=evt.getModifiers();
        if (tekanenter==10){
            try {
                sql="SELECT * FROM tb_kamar WHERE kode_kamar='"+ kode +"'";
                st=con.createStatement();
                rsKmr=st.executeQuery(sql);
                while (rsKmr.next()){
                    txtnamakamar.setText(rsKmr.getString("nama_kamar"));
                    cbtypekamar.setSelectedItem(rsKmr.getString(3));
                    txthargakamar.setText(rsKmr.getString(4));
                    JOptionPane.showMessageDialog(null, "data ditemukan");
                    btntambah.setEnabled(false);
                    btnsimpan.setEnabled(false);
                    
                }
            } catch (Exception e){
                JOptionPane.showMessageDialog(null, "tidak ketemu \n"+e.getMessage());
                txtnamakamar.requestFocus();
            }
        }
    }//GEN-LAST:event_txtkodekamarActionPerformed

